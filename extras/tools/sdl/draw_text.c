void DrawTextFreeFontTexture(void) {
  if (font_texture) {
    SDL_DestroyTexture(font_texture);
  }
}

void DrawText(SDL_Renderer* renderer, int x, int y, const char* format, ...) {
  char str[1024];
  va_list args;
  va_start(args, format);
  int num_chars = vsprintf(str, format, args);
  va_end(args);

  SDL_Rect source_rect;
  source_rect.x = 0;
  source_rect.y = 0;
  source_rect.w = kFontCharWidth;
  source_rect.h = kFontCharHeight;
  SDL_Rect dest_rect;
  dest_rect.x = x;
  dest_rect.y = y;
  dest_rect.w = kFontCharWidth;
  dest_rect.h = kFontCharHeight;

  int i;
  for (i = 0; i < num_chars; ++i, x += kFontCharWidth) {
    const unsigned char c = str[i];
    if (33 <= c && c < 33 + kFontNumChars) { /* A printable character. */
      /* `font_texture` is a tall image of all the chars with width kCharWidth
       * and height kCharHeight * kNumChars. We offset the `source_rect` to the
       * position of character `c`.
       */
      source_rect.y = kFontCharHeight * (c - 33);
      dest_rect.x = x;
      SDL_RenderCopy(renderer, font_texture, &source_rect, &dest_rect);
    }
  }
}

int DrawTextSetColor(uint8_t r, uint8_t g, uint8_t b) {
  return SDL_SetTextureColorMod(font_texture, r, g, b) == 0;
}
